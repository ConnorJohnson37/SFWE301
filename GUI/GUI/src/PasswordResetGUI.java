    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        if (userInput.getText().equals("answer")) {
            newLabel.setVisible(true);
            newField.setVisible(true);
            confirmLabel.setVisible(true);
            confirmField.setVisible(true);
            submitNew.setVisible(true);
            submitButton.setVisible(false);
            userInput.setVisible(false);
            securityQuestion.setVisible(false);
        }
        else {
            //wrong answer to security question so password cant be changed
            jLabel1.setText("Incorrect Password");
            submitButton.setVisible(false);
            userInput.setVisible(false);
            securityQuestion.setVisible(false);
        }
        
    }//GEN-LAST:event_submitButtonActionPerformed

