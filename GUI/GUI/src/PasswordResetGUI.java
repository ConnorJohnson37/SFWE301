    private void submitUsernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitUsernameActionPerformed
        if (usernameField.getText().equals("user")) {//code where checks if user exists
            //if user exists, update security question
            Random random = new Random();

        // Generate a random number between 1 and 3 (inclusive)
            int randomNumber = random.nextInt(3) + 1;
            if (randomNumber == 1) {
                securityQuestion.setText("If the question is the lock, what is the universal key?");
            }
            else if (randomNumber == 2) {
                securityQuestion.setText("When the enigma whispers, what's the echo that unravels mysteries?");
            }
            else {
                securityQuestion.setText("What's the one-word solution to every riddle in the world?" );
            }
            securityQuestion.setVisible(true);
            userInput.setVisible(true);
            submitButton.setVisible(true);
            submitUsername.setVisible(false);
            usernameField.setVisible(false);
            usernameLabel.setVisible(false);
        }
        else {
            //error where user doesn't exist
            //run until user found
        }
    }//GEN-LAST:event_submitUsernameActionPerformed

    private void submitNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitNewActionPerformed
        if (newField.getText().equals("")) {
            //if no input do nothing
        }
        else if (newField.getText().equals(confirmField.getText())) {
            //update password for user
            newLabel.setVisible(false);
            newField.setVisible(false);
            confirmLabel.setVisible(false);
            confirmField.setVisible(false);
            submitNew.setVisible(false);
            matchError.setVisible(false);
            jLabel1.setText("Password Reset");
            //redirect to main
        }
        else {
            //display passwords don't match error
            matchError.setVisible(true);
        }
    }//GEN-LAST:event_submitNewActionPerformed

